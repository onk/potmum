# frozen_string_literal: true

class ApplicationPolicy
  attr_reader :access_token, :record

  def initialize(access_token, record)
    @access_token = access_token
    @record = record
  end

  def user
    @access_token.try(:user)
  end

  def index?
    false
  end

  def show?
    scope.where(id: record.id).exists?
  end

  def create?
    false
  end

  def new?
    create?
  end

  def update?
    false
  end

  def edit?
    update?
  end

  def destroy?
    false
  end

  def scope
    Pundit.policy_scope!(user, record.class)
  end

  class Scope
    attr_reader :user, :scope

    def initialize(user, scope)
      @user = user
      @scope = scope
    end

    def resolve
      scope
    end
  end
end
